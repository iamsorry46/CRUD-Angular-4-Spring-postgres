export class Sopir {

  id: number;
  namaSopir: string;
  alamatSopir: string;
  kelaminSopir: string;
  lahirSopir: string;
  telpSopir: string;
 

  constructor(id: number, namaSopir: string, alamatSopir: string, kelaminSopir: string, lahirSopir: string, telpSopir: string){
    this.id = id;
    this.namaSopir = namaSopir;
    this.alamatSopir = alamatSopir;
    this.kelaminSopir = kelaminSopir;
    this.lahirSopir = lahirSopir;
    this.telpSopir = telpSopir;
    
  }

}